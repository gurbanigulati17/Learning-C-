using System;


    class Program
    {
 

  
           static void Main(string[] args)
        {  
            int a=10;
            int b=5;
             
             //Numbers And Integer Math
             //1. Addition Operator
             int c=a+b;
             Console.WriteLine(c);

             //2. Subtraction Operator
             int s=a-b;
             Console.WriteLine(s);


             //3. Division
             int d=a/b;
             Console.WriteLine(d);


             //4. Multiplication
             int m=a*b;
             Console.WriteLine(m);
          


            //Multiple Use of Operator in expression
            int exp=(a+b)/5*4;
            Console.WriteLine(exp);
        }
    }

