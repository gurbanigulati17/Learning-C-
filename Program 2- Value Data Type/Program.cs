using System;


    class Program
    {
 

    static void Main(string[] args)
        {
              // Three category of data types : Namely;
            //1. Value Data Types
            //2. Referenced Data Type
            //3. Pointer Data Types......................................

            //In this program we ll concentrate highly on Value Data Types.





            // Print integer value.
            int a=5;
            Console.WriteLine("Value of a is "+a);

            //Print String value.
            string name="Guru";
            Console.WriteLine("Value of string is "+name);

            //Print bool value.
            bool ans=true;
            Console.WriteLine("Value of boolean variable is "+ans);  


            //Char data Type
            char c='G';
            Console.WriteLine("Char value "+c);

            //short Data Type
            short s=78;
            Console.WriteLine("Short value "+s);


            //long dataType
            long l=7876545;
            Console.WriteLine("Long Value "+l);


            //float data type
            float f=345.374f;
            Console.WriteLine("Float Value "+f);

            //Decimal value 
            decimal d=245.3m;
            Console.WriteLine("Decimal value "+d);

            //uint value
            uint ui=23;
            Console.WriteLine("uint value is "+ui);


            //ulong value 
            ulong ul=3567789;
            Console.WriteLine("ulong value is "+ul);
          }
           
        }
    }

